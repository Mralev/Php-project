<h2>About Viza</h2>
<h3>The Sound</h3>
<p>Viza is an American rock band from Los Angeles, CA. They mix middle eastern intstruments with modern rock instruments to create a unique sound.
Viza mixes Armenian and Greek traditional elements with modern aggressive rock.</p>
<h3>Activity</h3>
<p>Viza has been active since 2000, since then they have re-invented thier style into a unique sound. Viza has toured in many countries as main acts and supporting acts. They continue to release new singles and albums and EPs.</p>
